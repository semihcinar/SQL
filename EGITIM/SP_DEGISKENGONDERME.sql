ALTER PROCEDURE SP_CARD_CONTROL
@WORKERBORDERCODE AS VARCHAR (50)


AS
BEGIN

	DECLARE @WORKERNAME AS VARCHAR (100)
	

	SELECT @WORKERNAME=WORKERNAME FROM WORKER WHERE WORKERBORDERCODE=@WORKERBORDERCODE
	IF @WORKERNAME IS NULL
	BEGIN
		SELECT 'KART GEÇERSÝZ'
	END
	ELSE
	BEGIN
		SELECT @WORKERNAME
	END
END



